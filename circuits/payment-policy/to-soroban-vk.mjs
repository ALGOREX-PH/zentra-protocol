// Emits the Soroban-side artifacts for the payment-policy circuit:
//   contracts/zentra-verifier/src/vk.rs              (verification key builder)
//   contracts/zentra-verifier/src/payment_fixtures.rs (sample proof + 14 public signals, test-only)
//
// Byte layout matches soroban-sdk v26 bn254 (see circuits/spike/to-soroban.mjs):
//   G1 = be(X)||be(Y) (64B); G2 = be(X.c1)||be(X.c0)||be(Y.c1)||be(Y.c0) (128B); Fr = 32B BE.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const read = (f) => JSON.parse(readFileSync(resolve(here, f), "utf8"));
const vk = read("verification_key.json");
const proof = read("proof.json");
const pub = read("public.json");

function be32(dec) {
  let n = BigInt(dec);
  const out = new Uint8Array(32);
  for (let i = 31; i >= 0; i--) { out[i] = Number(n & 0xffn); n >>= 8n; }
  if (n !== 0n) throw new Error(`value exceeds 32 bytes: ${dec}`);
  return out;
}
const cat = (...a) => { const t = a.reduce((s, x) => s + x.length, 0); const o = new Uint8Array(t); let k = 0; for (const x of a) { o.set(x, k); k += x.length; } return o; };
const g1 = (p) => cat(be32(p[0]), be32(p[1]));
const g2 = (p) => cat(be32(p[0][1]), be32(p[0][0]), be32(p[1][1]), be32(p[1][0]));
const arr = (name, b) => `pub const ${name}: [u8; ${b.length}] = [${Array.from(b).join(", ")}];`;
const arr2 = (name, rows) => `pub const ${name}: [[u8; ${rows[0].length}]; ${rows.length}] = [\n${rows.map((b) => `    [${Array.from(b).join(", ")}]`).join(",\n")},\n];`;

const ic = vk.IC.map(g1);
const vkRs = `// @generated by circuits/payment-policy/to-soroban-vk.mjs — DO NOT EDIT BY HAND.
// Groth16/BN254 verification key for payment_policy.circom (${ic.length} IC points, ${ic.length - 1} public inputs).
use soroban_sdk::{
    crypto::bn254::{Bn254G1Affine as G1Affine, Bn254G2Affine as G2Affine},
    Env, Vec,
};

use crate::groth16::VerificationKey;

${arr("ALPHA", g1(vk.vk_alpha_1))}
${arr("BETA", g2(vk.vk_beta_2))}
${arr("GAMMA", g2(vk.vk_gamma_2))}
${arr("DELTA", g2(vk.vk_delta_2))}
${arr2("IC", ic)}

/// Build the payment-policy verification key in soroban-sdk byte form.
pub fn verification_key(env: &Env) -> VerificationKey {
    let mut ic = Vec::new(env);
    for p in IC.iter() {
        ic.push_back(G1Affine::from_array(env, p));
    }
    VerificationKey {
        alpha: G1Affine::from_array(env, &ALPHA),
        beta: G2Affine::from_array(env, &BETA),
        gamma: G2Affine::from_array(env, &GAMMA),
        delta: G2Affine::from_array(env, &DELTA),
        ic,
    }
}
`;
writeFileSync(resolve(here, "../../contracts/zentra-verifier/src/vk.rs"), vkRs);

const pubRows = pub.map(be32);
const fxRs = `// @generated by circuits/payment-policy/to-soroban-vk.mjs — DO NOT EDIT BY HAND.
// Sample payment-policy proof + its ${pubRows.length} public signals (test-only fixture).
#![allow(dead_code)]

${arr("PROOF_A", g1(proof.pi_a))}
${arr("PROOF_B", g2(proof.pi_b))}
${arr("PROOF_C", g1(proof.pi_c))}

${arr2("PUB_SIGNALS", pubRows)}
`;
writeFileSync(resolve(here, "../../contracts/zentra-verifier/src/payment_fixtures.rs"), fxRs);

console.log(`wrote vk.rs (${ic.length} IC points) and payment_fixtures.rs (${pubRows.length} public signals)`);
